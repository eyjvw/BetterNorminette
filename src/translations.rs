//! Every norminette error code with its official English message and the
//! French / Spanish translations. Codes taken from 42School/norminette
//! norm_error.py (138 entries).

#[derive(Clone, Copy, PartialEq, Eq)]
pub enum Lang {
    En,
    Fr,
    Es,
}

impl Lang {
    pub fn parse(s: &str) -> Option<Lang> {
        match s.to_lowercase().as_str() {
            "en" | "english" | "anglais" | "ingles" | "inglés" => Some(Lang::En),
            "fr" | "french" | "francais" | "français" | "frances" | "francés" => Some(Lang::Fr),
            "es" | "spanish" | "espagnol" | "espanol" | "español" => Some(Lang::Es),
            _ => None,
        }
    }

    pub fn code(&self) -> &'static str {
        match self {
            Lang::En => "en",
            Lang::Fr => "fr",
            Lang::Es => "es",
        }
    }
}

