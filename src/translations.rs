pub fn translate(code: &str, lang: Lang) -> Option<&'static str> {
    ERRORS.iter().find(|(c, ..)| *c == code).map(|(_, en, fr, es)| match lang {
        Lang::En => *en,
        Lang::Fr => *fr,
        Lang::Es => *es,
    })
}

